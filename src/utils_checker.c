#include "push_swap.h"

int	is_digit(char c)
{
	return (c >= '0' && c <= '9');
}

int	is_sign(char c)
{
	return (c == '+' || c == '-');
}

int	nbstr_cmp(const char *s1, const char *s2)
{
	int	a;
	int	b;

	a = ft_atoi(s1);
	b = ft_atoi(s2);
	return (a != b);
}
